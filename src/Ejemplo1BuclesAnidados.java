/*
 * Programa que dibuja un rectángulo sólido de asteriscos.
 * El número de filas y columnas se pide por teclado
 */
import java.util.*;
public class Ejemplo1BuclesAnidados {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int filas, columnas;

	//No para de ejecutarse el bucle hasta que el numero introducido sea mayor que 0
        do{
           System.out.print("Introduce número de filas: ");
           filas = sc.nextInt();
        }while(filas<1);
        //No para de ejecutarse el bucle hasta que el numero introducido sea mayor que 0
        do{
           System.out.print("Introduce número de columnas: ");
           columnas = sc.nextInt();
        }while(columnas<1);

        //valor inicial de i es 1, cuando i es menor o igual al valor de filas ejecuta el bucle. 
	//Por cada vez que se ejecuta el bucle el valor de i se incrementa en 1
        for(int i = 1; i<=filas; i++){    //filas

	//valor inicial de j es 1, cuando j es menor o igual al valor de columnas ejecuta el bucle
	//Por cada vez que se ejecuta el bucle el valor de j se incrementa en 1
            for(int j = 1; j<=columnas; j++){   //columnas
                 System.out.print(" * ");
            }
	//realiza un salto de linea
            System.out.println();
        }
       
    }
}
